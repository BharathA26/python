# An arithmetic operator is a symbol that represents a mathematical operation, like addition, subtraction, multiplication, or division. These operators are used to perform calculations on numbers in programming languages and other mathematical contexts. 
# In Python, the following arithmetic operators are commonly used:
# 1. Addition (+): Adds two numbers together.   
# 2. Subtraction (-): Subtracts one number from another.
# 3. Multiplication (*): Multiplies two numbers together.   
# 4. Division (/): Divides one number by another and returns a float.       
# 5. modulus (%): Returns the remainder of a division operation.    
# 6. exponentiation (**): Raises a number to the power of another number.

# example of arithmetic operator
friends = 5
friends = friends + 1
print(friends)  # Output: 6
friends += 1    
print(friends)  # Output: 7
friends = friends - 1
print(friends)  # Output: 6 
friends -= 1
print(friends)  # Output: 5
friends = friends * 2
print(friends)  # Output: 10    
friends *= 2
print(friends)  # Output: 10    
friends = friends / 2
print(friends)  # Output: 5.0
friends /= 2
print(friends)  # Output: 5.0
friends = friends ** 2
print(friends)  # Output: 25.0
friends **= 2   
print(friends)  # Output: 25.0
friends = friends % 2
print(friends)  # Output: 1.0
friends %= 2
print(friends)  # Output: 1.0


